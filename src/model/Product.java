package model;

public class Product {
	private int prod_no;
	private String prod_name;
	private String prod_nation;
	private String prod_theme;
	private String prod_grade;
	private int shop_no;
	private String prod_del;
	
	
	public int getProd_no() {
		return prod_no;
	}
	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public String getProd_nation() {
		return prod_nation;
	}
	public void setProd_nation(String prod_nation) {
		this.prod_nation = prod_nation;
	}
	public String getProd_theme() {
		return prod_theme;
	}
	public void setProd_theme(String prod_theme) {
		this.prod_theme = prod_theme;
	}
	public String getProd_grade() {
		return prod_grade;
	}
	public void setProd_grade(String prod_grade) {
		this.prod_grade = prod_grade;
	}
	public int getShop_no() {
		return shop_no;
	}
	public void setShop_no(int shop_no) {
		this.shop_no = shop_no;
	}
	public String getProd_del() {
		return prod_del;
	}
	public void setProd_del(String prod_del) {
		this.prod_del = prod_del;
	}
	
}
