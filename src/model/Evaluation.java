package model;

public class Evaluation {
	private int shop_no;
	private int cust_no;
	private String eval_list;
	private String eval_grade;
	private int prod_no;
	private String eval_del;
	
	
	public int getShop_no() {
		return shop_no;
	}
	public void setShop_no(int shop_no) {
		this.shop_no = shop_no;
	}
	public int getCust_no() {
		return cust_no;
	}
	public void setCust_no(int cust_no) {
		this.cust_no = cust_no;
	}
	public String getEval_list() {
		return eval_list;
	}
	public void setEval_list(String eval_list) {
		this.eval_list = eval_list;
	}
	public String getEval_grade() {
		return eval_grade;
	}
	public void setEval_grade(String eval_grade) {
		this.eval_grade = eval_grade;
	}
	public int getProd_no() {
		return prod_no;
	}
	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}
	public String getEval_del() {
		return eval_del;
	}
	public void setEval_del(String eval_del) {
		this.eval_del = eval_del;
	}
	
	
}
