package model;

import java.sql.Date;

public class Customer {
	private int cust_no;
	private String cust_id;
	private String cust_pw;
	private String cust_name;
	private String cust_nick;
	private Date cust_birth;
	private String cust_tel;
	private String cust_addr;
	private String cust_grade;
	private int cust_point;
	private String cust_out;
	private String cust_del;
	
	
	public int getCust_no() {
		return cust_no;
	}
	public void setCust_no(int cust_no) {
		this.cust_no = cust_no;
	}
	public String getCust_id() {
		return cust_id;
	}
	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
	public String getCust_pw() {
		return cust_pw;
	}
	public void setCust_pw(String cust_pw) {
		this.cust_pw = cust_pw;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public String getCust_nick() {
		return cust_nick;
	}
	public void setCust_nick(String cust_nick) {
		this.cust_nick = cust_nick;
	}
	public Date getCust_birth() {
		return cust_birth;
	}
	public void setCust_birth(Date cust_birth) {
		this.cust_birth = cust_birth;
	}
	public String getCust_tel() {
		return cust_tel;
	}
	public void setCust_tel(String cust_tel) {
		this.cust_tel = cust_tel;
	}
	public String getCust_addr() {
		return cust_addr;
	}
	public void setCust_addr(String cust_addr) {
		this.cust_addr = cust_addr;
	}
	public String getCust_grade() {
		return cust_grade;
	}
	public void setCust_grade(String cust_grade) {
		this.cust_grade = cust_grade;
	}
	public int getCust_point() {
		return cust_point;
	}
	public void setCust_point(int cust_point) {
		this.cust_point = cust_point;
	}
	public String getCust_out() {
		return cust_out;
	}
	public void setCust_out(String cust_out) {
		this.cust_out = cust_out;
	}
	public String getCust_del() {
		return cust_del;
	}
	public void setCust_del(String cust_del) {
		this.cust_del = cust_del;
	}
	
	
		
}
