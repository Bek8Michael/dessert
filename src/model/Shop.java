package model;

import java.sql.Date;

public class Shop {
	private int shop_no;
	private String shop_name;
	private String shop_loc;
	private String shop_addr;
	private String shop_tel;
	private String shop_worktime;
	private String shop_menu;
	private String shop_grade;
	private Date shop_uptime;
	private String shop_nation;
	private String shop_del;
	
	public int getShop_no() {
		return shop_no;
	}
	public void setShop_no(int shop_no) {
		this.shop_no = shop_no;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public String getShop_loc() {
		return shop_loc;
	}
	public void setShop_loc(String shop_loc) {
		this.shop_loc = shop_loc;
	}
	public String getShop_addr() {
		return shop_addr;
	}
	public void setShop_addr(String shop_addr) {
		this.shop_addr = shop_addr;
	}
	public String getShop_tel() {
		return shop_tel;
	}
	public void setShop_tel(String shop_tel) {
		this.shop_tel = shop_tel;
	}
	public String getShop_worktime() {
		return shop_worktime;
	}
	public void setShop_worktime(String shop_worktime) {
		this.shop_worktime = shop_worktime;
	}
	public String getShop_menu() {
		return shop_menu;
	}
	public void setShop_menu(String shop_menu) {
		this.shop_menu = shop_menu;
	}
	public String getShop_grade() {
		return shop_grade;
	}
	public void setShop_grade(String shop_grade) {
		this.shop_grade = shop_grade;
	}
	public Date getShop_uptime() {
		return shop_uptime;
	}
	public void setShop_uptime(Date shop_uptime) {
		this.shop_uptime = shop_uptime;
	}
	public String getShop_nation() {
		return shop_nation;
	}
	public void setShop_nation(String shop_nation) {
		this.shop_nation = shop_nation;
	}
	public String getShop_del() {
		return shop_del;
	}
	public void setShop_del(String shop_del) {
		this.shop_del = shop_del;
	}
	
	
}
